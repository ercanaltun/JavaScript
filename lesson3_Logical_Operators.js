var num1 =5;
var num2 = 9;

console.log(num1==5);      //true döner 
console.log(num1=="5");   // true döner      " == eşittir işareti sadece verinin eşit olup olmadığını kontrol eder."


console.log(num2===9);    //true döner
console.log(num2==="9");  //false döner     " === eşittir işareti hem verinin hem de veri tipinin eşit olup olmadığını kontrol eder."


console.log(num1>4);       //true döner
console.log(num1>"4");    // true döner      " > , < , >= , <= , != işaretleri sadece verinin eşit olup olmadığını kontrol eder."

